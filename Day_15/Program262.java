import java.util.*;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name      : ArrayX
//  Description     : Contains array and functions to accept, display and find maximum element.
//  Input            : Integer Array
//  Output           : Displays array elements and maximum element
//  Author           : Gavade Somanath Vilas
//  Date             : 08/05/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class ArrayX
{
    public int Arr[];

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : ArrayX (Parameterized Constructor)
//  Description      : Initializes array with given size.
//  Input            : Integer
//  Output           : Allocates memory for array
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public ArrayX(int iSize)
    {
        Arr = new int[iSize];
    }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Accept
//  Description      : Accepts elements from user and stores them into array.
//  Input            : None (Uses class data member)
//  Output           : Stores elements into array
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public void Accept()
    {
        int i = 0;

        Scanner sobj = new Scanner(System.in);

        System.out.println("Enter elements : ");

        for(i = 0; i < Arr.length; i++)
        {
            Arr[i] = sobj.nextInt();
        }
    }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display
//  Description      : Displays all elements of the array.
//  Input            : None (Uses class data member)
//  Output           : Prints array elements on screen
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public void Display()
    {
        int i = 0;

        System.out.println("Elements of the Array are : ");

        for(i = 0; i < Arr.length; i++)
        {
            System.out.println(Arr[i]);
        }
    }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Maximum
//  Description      : Finds maximum element from array.
//  Input            : None (Uses class data member)
//  Output           : Returns maximum element from array
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public int Maximum()
    {
        int iMax = Arr[0], i = 0;

        for(i = 0; i < Arr.length; i++)
        {
            if(iMax < Arr[i])
            {
                iMax = Arr[i];
            }
        }

        return iMax;
    }
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name      : Program262
//  Description     : This application accepts array elements and finds maximum element.
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Program262
{
    public static void main(String Arg[])
    {
        Scanner sobj = new Scanner(System.in);

        int iLength = 0, iRet = 0;

        System.out.println("Enter a number of elements : ");
        iLength = sobj.nextInt();

        ArrayX aobj = new ArrayX(iLength);

        aobj.Accept();
        aobj.Display();

        iRet = aobj.Maximum();

        System.out.println("Maximum of all elements : " + iRet);
    }
}