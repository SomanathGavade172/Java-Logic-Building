import java.util.*;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name      : Digits
//  Description     : Contains function to count frequency of digit 7.
//  Input           : Integer
//  Output          : Displays frequency of digit 7
//  Author          : Gavade Somanath Vilas
//  Date            : 07/05/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Digits 
{

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : CountDigits
//  Description     : Counts frequency of digit 7 in given number.
//  Input           : Integer
//  Output          : Displays frequency of digit 7
//  Time Complexity : O(N)
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public void CountDigits(int iNo)
    {
        int iDigit = 0, iCount = 0;

        while(iNo != 0)
        {
            iDigit = iNo % 10;

            if(iDigit == 7)
            {
                iCount++;
            }

            iNo = iNo / 10;
        }

        System.out.println("Frequency of 7 is : " + iCount);
    }
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name      : Program245
