//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Program245
{
    public static void main(String Arg[])
    {
        Scanner sobj = new Scanner(System.in);

        int iValue = 0;

        System.out.println("Enter a number : ");
        iValue = sobj.nextInt();

        Digits dobj = new Digits();

        dobj.CountDigits(iValue);

    }
}