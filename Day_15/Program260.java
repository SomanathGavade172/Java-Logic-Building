import java.util.*;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name      : ArrayX
//  Description     : Contains array and functions to accept and display array elements.
//  Input            : Integer
//  Output           : Accepts and displays array elements
//  Author           : Gavade Somanath Vilas
//  Date             : 08/05/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class ArrayX
{
    public int Arr[];

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : ArrayX (Parameterized Constructor)
//  Description      : Initializes array with given size.
//  Input            : Integer
//  Output           : Allocates memory for array
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public ArrayX(int iSize)
    {
        Arr = new int[iSize];
    }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Accept
//  Description      : Accepts elements from user and stores them into array.
//  Input            : None (Uses class data member)
//  Output           : Stores elements into array
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public void Accept()
    {
        int i = 0;

        Scanner sobj = new Scanner(System.in);

        System.out.println("Enter elements : ");

        for(i = 0; i < Arr.length; i++)
        {
            Arr[i] = sobj.nextInt();
        }
    }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display
//  Description      : Displays all elements of the array.
//  Input            : None (Uses class data member)
//  Output           : Prints array elements on screen
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public void Display()
    {
        int i = 0;

        System.out.println("Elements of the Array are : ");

        for(i = 0; i < Arr.length; i++)
        {
            System.out.println(Arr[i]);
        }
    }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : finalize
//  Description      : Performs cleanup activities before object destruction.
//  Input            : None
//  Output           : Releases array reference
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    protected void finalize()
    {
        Arr = null;
        System.out.println("Inside finalize");
    }
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name      : Program260
//  Description     : This application demonstrates garbage collection and finalize method.
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Program260
{
    public static void main(String Arg[])
    {
        Scanner sobj = new Scanner(System.in);

        int iLength = 0, i = 0;

        System.out.println("Enter a number of elements : ");
        iLength = sobj.nextInt();

        ArrayX aobj = new ArrayX(iLength);

        aobj.Accept();
        aobj.Display();

        aobj = null;

        System.gc();
    }
}