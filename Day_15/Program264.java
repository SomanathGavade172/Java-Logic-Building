import java.util.*;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name      : ArrayX
//  Description     : Contains array and functions to accept, display and count digits of each element.
//  Input            : Integer Array
//  Output           : Displays array elements and digit count of each element
//  Author           : Gavade Somanath Vilas
//  Date             : 08/05/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class ArrayX
{
    public int Arr[];

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : ArrayX (Parameterized Constructor)
//  Description      : Initializes array with given size.
//  Input            : Integer
//  Output           : Allocates memory for array
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public ArrayX(int iSize)
    {
        Arr = new int[iSize];
    }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Accept
//  Description      : Accepts elements from user and stores them into array.
//  Input            : None (Uses class data member)
//  Output           : Stores elements into array
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public void Accept()
    {
        int i = 0;

        Scanner sobj = new Scanner(System.in);

        System.out.println("Enter elements : ");

        for(i = 0; i < Arr.length; i++)
        {
            Arr[i] = sobj.nextInt();
        }
    }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display
//  Description      : Displays all elements of the array.
//  Input            : None (Uses class data member)
//  Output           : Prints array elements on screen
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public void Display()
    {
        int i = 0;

        System.out.println("Elements of the Array are : ");

        for(i = 0; i < Arr.length; i++)
        {
            System.out.println(Arr[i]);
        }
    }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : CountDigits
//  Description      : Counts number of digits of each array element.
//  Input            : None (Uses class data member)
//  Output           : Displays digit count of each element
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public void CountDigits()
    {
        int iCount = 0, i = 0, iNo = 0;

        for(i = 0; i < Arr.length; i++)
        {
            iNo = Arr[i];

            while(iNo != 0)
            {
                iCount++;

                iNo = iNo / 10;
            }

            System.out.println(iCount);

            iCount = 0;
        }
    }
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name      : Program264
//  Description     : This application accepts array elements and counts digits of each element.
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Program264
{
    public static void main(String Arg[])
    {
        Scanner sobj = new Scanner(System.in);

        int iLength = 0;

        System.out.println("Enter a number of elements : ");
        iLength = sobj.nextInt();

        ArrayX aobj = new ArrayX(iLength);

        aobj.Accept();
        aobj.Display();

        aobj.CountDigits();
    }
}