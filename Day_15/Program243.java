import java.util.*;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name      : Digits
//  Description     : Contains function to calculate addition of digits.
//  Input           : Integer
//  Output          : Returns addition of digits
//  Author          : Gavade Somanath Vilas
//  Date            : 07/05/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Digits 
{

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : AdditionDigits
//  Description     : Calculates addition of digits of given number.
//  Input           : Integer
//  Output          : Integer
//  Time Complexity : O(N)
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public int AdditionDigits(int iNo)
    {
        int iDigit = 0, iSum = 0;

        while(iNo != 0)
        {
            iDigit = iNo % 10;

            iSum = iSum + iDigit;

            iNo = iNo / 10;
        }

        return iSum;
    }
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name      : Program243
//  Description     : This application accepts number from user and displays addition of digits.
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Program243
{
    public static void main(String Arg[])
    {
        Scanner sobj = new Scanner(System.in);

        int iValue = 0, iRet = 0;

        System.out.println("Enter a number : ");
        iValue = sobj.nextInt();

        Digits dobj = new Digits();

        iRet = dobj.AdditionDigits(iValue);

        System.out.println("Addition of Digits are : " + iRet);

    }
}