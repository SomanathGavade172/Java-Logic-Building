import java.util.*;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Calculations
//  Description      : Contains function to perform percentage calculation.
//  Input            : Integer values
//  Output           : Returns calculated percentage
//  Author           : Gavade Somanath Vilas
//  Date             : 23/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Calculations
{

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : CalculatePercentage()
//  Description      : Calculates percentage using obtained and total marks.
//  Input            : Integer, Integer
//  Output           : Float (Returns calculated percentage)
//  Time Complexity  : O(1)
//  Date             : 23/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // Function definition
    public float CalculatePercentage(int A, int B)
    {
        float fResult = 0.0f;

        fResult = ((float) A / (float) B ) * 100;

        return fResult;
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Percentage Calculation using OOP
//  Description      : This application accepts obtained marks and total marks from user and calculates percentage.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Program233
{
    public static void main(String Arg[])
    {
        int iNo1 = 0, iNo2 = 0;
        float fAns = 0.0f;

        Scanner sobj = new Scanner(System.in);

        System.out.println("Enter Obtained Marks : ");
        iNo1 = sobj.nextInt();

        System.out.println("Enter Total Marks : ");
        iNo2 = sobj.nextInt();

        Calculations cobj = new Calculations();

        fAns = cobj.CalculatePercentage(iNo1, iNo2);

        System.out.println("Percentage is : " + fAns);
    }
}