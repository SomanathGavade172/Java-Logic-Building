class Arithematic
{

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Addition()
//  Description      : Calculates power of a number (A raised to B).
//  Input            : Integer, Integer
//  Output           : Long (Returns A^B)
//  Time Complexity  : O(N)
//  Date             : 23/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // Function definition
    public long Addition(int A, int B)
    {
        int iCnt = 0;
        long iMul = 1;

        for(iCnt = 1; iCnt <= B; iCnt++)
        {
            iMul = iMul * A;
        }
        
        return iMul;
    }
}

