class Rectagle
{

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : CalculateArea()
//  Description      : Calculates area using length and width.
//  Input            : Float, Float
//  Output           : Float (Returns calculated area)
//  Time Complexity  : O(1)
//  Date             : 23/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // Function definition
    public float CalculateArea(float A, float B)
    {
        float fResult = 0.0f;

        fResult = A * B;

        return fResult;
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Area Calculation using OOP
//  Description      : This application accepts length and width from user and calculates area.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Program235
{
    public static void main(String Arg[])
    {
        float fNo1 = 0.0f, fNo2 = 0.0f;
        float fAns = 0.0f;

        Scanner sobj = new Scanner(System.in);

        System.out.println("Enter Length : ");
        fNo1 = sobj.nextFloat();

        System.out.println("Enter Width : ");
        fNo2 = sobj.nextFloat();

        Rectagle cobj = new Rectagle();

        fAns = cobj.CalculateArea(fNo1, fNo2);

        System.out.println("Area is : " + fAns);
    }
}