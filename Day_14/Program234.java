class Calculations
{

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : CalculateArea()
//  Description      : Calculates area using length and width.
//  Input            : Integer, Integer
//  Output           : Integer (Returns calculated area)
//  Time Complexity  : O(1)
//  Date             : 23/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // Function definition
    public int CalculateArea(int A, int B)
    {
        int iResult = 0;

        iResult = A * B;

        return iResult;
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Area Calculation using OOP
//  Description      : This application accepts length and width from user and calculates area.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Program234
{
    public static void main(String Arg[])
    {
        int iNo1 = 0, iNo2 = 0;
        int iAns = 0;

        Scanner sobj = new Scanner(System.in);

        System.out.println("Enter Length : ");
        iNo1 = sobj.nextInt();

        System.out.println("Enter Width : ");
        iNo2 = sobj.nextInt();

        Calculations cobj = new Calculations();

        iAns = cobj.CalculateArea(iNo1, iNo2);

        System.out.println("Area is : " + iAns);
    }
}